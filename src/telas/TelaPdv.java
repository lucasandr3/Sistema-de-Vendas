    private void jm_pdv_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jm_pdv_excluirActionPerformed
        if (table_products.getRowCount() > 0) {
            DefaultTableModel model = (DefaultTableModel) this.table_products.getModel();
            int linha = Integer.parseInt(JOptionPane.showInputDialog("Informe o item que deseja excluir."));
            model.removeRow(linha - 1);
            //subtotal_label.setText(String.valueOf(somaValorTotal()));
            int quantLinhas = table_products.getRowCount();
            for (int i = 0; i < quantLinhas; i++) {
                model.setValueAt(i + 1, i, 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Não existe itens na lista.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jm_pdv_excluirActionPerformed

