    private float somaValorTotal() {
        float soma = 0, valor = 0;
        int count = table_products.getRowCount();

        for (int i = 0; i < count; i++) {
            valor = Float.parseFloat(String.valueOf(table_products.getValueAt(i, 2)));
            //formatador.converterVirgulaParaPonto(String.valueOf(valor));
            soma += valor;
        }
        return soma;
    }

