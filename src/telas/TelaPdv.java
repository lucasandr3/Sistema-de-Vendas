    private void insereProdutoPdv(java.awt.event.KeyEvent e) {
        this.jlstatus.setText("Ocupado");
        DefaultTableModel model = (DefaultTableModel) table_products.getModel();
        this.setRowTableAlign(5);
        double vTotal;
        String verifyEmpty = inp_keyItem.getText();
        cupom.setModel(mList);

        if (e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {

            if (!verifyEmpty.equals("")) {

                try {

                    String codigoProduto;
                    codigoProduto = inp_keyItem.getText();
                    modelProdutos = controllerProdutos.getProdutosController(codigoProduto);

                    if (modelProdutos.getIdProd() > 0) {
                        vTotal = modelProdutos.getPreco() * quantidade;
                        model.addRow(new Object[]{
                            model.getRowCount() + 1,
                            modelProdutos.getNome_prod(),
                            String.valueOf(modelProdutos.getPreco()),
                            quantidade,
                            vTotal
                        });

                        mList.addElement(
                            "Produto:"+modelProdutos.getNome_prod().trim() +"Qtd:"+quantidade+ "\n"+"Valor Unit:"+modelProdutos.getPreco()
                        );

                        if (modelProdutos.getImagem().equals("default.png")) {

                            ImageIcon imageIcon = new ImageIcon(new ImageIcon(caminho+"/default.png").
                                    getImage().getScaledInstance(230, 230, Image.SCALE_DEFAULT)
                            );
                            fotoProdutoPDV.setIcon(imageIcon);

                        } else {

                            ImageIcon imageIcon = new ImageIcon(new ImageIcon(caminho+ modelProdutos.getImagem()).
                                    getImage().getScaledInstance(230, 230, Image.SCALE_DEFAULT)
                            );
                            fotoProdutoPDV.setIcon(imageIcon);
                        }

                        subtotal_label.setText(String.valueOf(somaValorTotal()));
                        inp_keyItem.setText("");
                        quantidade = 1;

                    } else {
                        JOptionPane.showMessageDialog(this, "Produto não encontrado, verifique se o produto existe.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                        inp_keyItem.setText("");
                    }
                } catch (HeadlessException ex) {
                    JOptionPane.showMessageDialog(this, "Você deve informar apenas números", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    inp_keyItem.setText("");
                }

            } else {
                //JOptionPane.showMessageDialog(this, "Você deve informar um produto", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                inp_keyItem.setText("");
            }
        }
    }

