    public void listaClientes() {
        listaClientes = clienteController.getListaClienteController();
        DefaultTableModel model = (DefaultTableModel) tabela_clientesAtivos.getModel();
        model.setNumRows(0);
        int count = listaClientes.size();
        for (int i = 0; i < count; i++) {
            model.addRow(new Object[]{
                listaClientes.get(i).getIdCliente(),
                listaClientes.get(i).getName().toUpperCase(),
                listaClientes.get(i).getPhone(),
                listaClientes.get(i).getBirthdate(),});
        }
    }

