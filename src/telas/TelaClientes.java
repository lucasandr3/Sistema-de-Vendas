    private void salvarCliente() {

        cliente.setName(nome_cliente.getText());
        cliente.setEmail(email_cliente.getText());
        cliente.setPhone(telefone_cliente.getText());
        cliente.setBirthdate(aniversario_cliente.getText());
        cliente.setAddressZipcode(cep_cliente.getText());
        cliente.setAddress(rua_cliente.getText());
        cliente.setAddressNumber(numero_cliente.getText());
        cliente.setAddressNeighb(bairro_cliente.getText());
        cliente.setAddressCity(cidade_araguari.getText());
        cliente.setDateCreate(cadastro_cliente.getText());

        if (clienteController.salvarClienteController(cliente) > 0) {
            JOptionPane.showMessageDialog(this, "Cliente Cadastrado com Sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.listaClientes();
            this.habilitarDesabilitarCampos(false);
            this.limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Cadastrar Cliente.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void alterarCliente() {

        cliente.setName(nome_cliente.getText());
        cliente.setEmail(email_cliente.getText());
        cliente.setPhone(telefone_cliente.getText());
        cliente.setBirthdate(aniversario_cliente.getText());
        cliente.setAddressZipcode(cep_cliente.getText());
        cliente.setAddress(rua_cliente.getText());
        cliente.setAddressNumber(numero_cliente.getText());
        cliente.setAddressNeighb(bairro_cliente.getText());
        cliente.setAddressCity(cidade_araguari.getText());
        cliente.setDateCreate(cadastro_cliente.getText());

        if (clienteController.atualizarClienteController(cliente)) {
            JOptionPane.showMessageDialog(this, "Cliente Atualizado com Sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.listaClientes();
            this.habilitarDesabilitarCampos(false);
            this.limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Atualizar Cliente.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }

