    private void salvarprodutos() {

        produtos.setNovo_cod(codigo_produto.getText());
        produtos.setNome_prod(nome_prod.getText());
        produtos.setMarca(String.valueOf(marca_prod.getSelectedItem()));
        produtos.setCategoria(String.valueOf(categoria_prod.getSelectedItem()));
        produtos.setUnidade(String.valueOf(unidade_prod.getSelectedItem()));
        produtos.setCost(Double.parseDouble(custo_prod.getText()));
        produtos.setPreco(Double.parseDouble(preco_prod.getText()));
        produtos.setAlert(Integer.parseInt(alert_prod.getText()));
        produtos.setValidateProd(validade_prod.getText());
        produtos.setImagem(nomeImagem);

        if (controllerProdutos.salvarProdutosController(produtos) > 0) {
            JOptionPane.showMessageDialog(this, "produtos Cadastrado com Sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            enviaImagemPasta();
            this.listaprodutos();
            this.habilitarDesabilitarCampos(false);
            this.limparCampos();
            nomeImagem = "";
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Cadastrar produtos.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }

