    private void jtfpesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfpesquisaKeyPressed

        DefaultTableModel model = (DefaultTableModel) tabela_categorias.getModel();
        String verifyEmpty = jtfpesquisa.getText();
        String status;
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {

            if (!verifyEmpty.equals("")) {

                try {

                    String nomeCategoria;
                    nomeCategoria = jtfpesquisa.getText();
                    modelCategorias = controllerCategorias.getCategoriaNomeController(nomeCategoria);

                    if (!modelCategorias.getNameCat().equals("")) {
                        model.setNumRows(0);
                        String statusTable;

                        if (modelCategorias.getStatus() == 0) {
                            statusTable = "Ativa";
                        } else {
                            statusTable = "Inativa";
                        }
                        model.addRow(new Object[]{
                            modelCategorias.getIdCat(),
                            modelCategorias.getNameCat(),
                            statusTable
                        });

                    } else {
                        JOptionPane.showMessageDialog(this, "Categoria n√£o encontrado, verifique se a categoria existe.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                        jtfpesquisa.setText("");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Categoria n√£o encontrada, verifique se a categoria existe.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    jtfpesquisa.setText("");
                    this.carregarCategorias();
                }

            } else {
                //JOptionPane.showMessageDialog(this, "Voc√™ deve informar um produto", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                jtfpesquisa.setText("");
                this.carregarCategorias();
            }
        }
    }//GEN-LAST:event_jtfpesquisaKeyPressed

