    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        salvarAtualizar = "alterar";
        int linha = tabela_produtosAtivos.getSelectedRow();
        
        try {
            String codigoProduto;
            codigoProduto = (String) tabela_produtosAtivos.getValueAt(linha, 0);

            //recupera o produto
            produtos = controllerProdutos.getProdutosController(codigoProduto);
            // set na interface
            codigo_produto.setText(String.valueOf(produtos.getNovo_cod()));
            nome_prod.setText(produtos.getNome_prod().toUpperCase());
            marca_prod.setSelectedItem(produtos.getMarca());
            categoria_prod.setSelectedItem(produtos.getCategoria());
            unidade_prod.setSelectedItem(produtos.getUnidade());
            custo_prod.setText(String.valueOf(produtos.getCost()));
            preco_prod.setText(String.valueOf(produtos.getPreco()));
            quantidade_prod.setText(String.valueOf(produtos.getQuantidade()));
            alert_prod.setText(String.valueOf(produtos.getAlert()));
            if (produtos.getValidateProd() == null) {
                validade_prod.setText("00/00/0000");
            } else {
                validade_prod.setText(String.valueOf(produtos.getValidateProd()));
            }

            if (produtos.getImagem().equals("default.png")) {

                ImageIcon imageIcon = new ImageIcon(new ImageIcon(caminho + "/default.png").
                        getImage().getScaledInstance(230, 230, Image.SCALE_DEFAULT)
                );
                fotoProduto.setIcon(imageIcon);

            } else {

                ImageIcon imageIcon = new ImageIcon(new ImageIcon(caminho + produtos.getImagem()).
                        getImage().getScaledInstance(230, 230, Image.SCALE_DEFAULT)
                );
                fotoProduto.setIcon(imageIcon);
            }

            this.habilitarDesabilitarCampos(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "C√≥digo inv√°lido ou nenhum produtos selecionado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_alterarActionPerformed

