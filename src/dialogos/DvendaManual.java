    private void calculaVenda(String param) {
        double valorc, valorDec, result;
        try {
            valorDec = Double.parseDouble(param);
            valorc = Double.parseDouble(ck_valorRestante.getText());
            result = valorc - valorDec;
            ck_valorRestante.setText(String.valueOf(result));
        } catch (NumberFormatException e) {
            System.out.println(e.getMessage());
        }

    }

