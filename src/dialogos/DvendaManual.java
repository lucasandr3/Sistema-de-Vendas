    private void combobox1cartaocreditoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combobox1cartaocreditoItemStateChanged
        String pg[] = combobox1cartaocredito.getSelectedItem().toString().split("-");
        try {
            float v = Float.parseFloat(ccredito1.getText());
            float t = Float.parseFloat(pg[1].trim());
            float valorTaxa = taxaPag.taxaPagamento(v, t);
            mVendaPagamentoCheckout.setCartaoCreditoLojaHist(ccredito1.getText());
            mVendaPagamentoCheckout.setCartaoCreditoLoja(String.valueOf(valorTaxa));
            mVendaPagamentoCheckout.setParcelaCreditoLoja(pg[0].trim());
            mVendaPagamentoCheckout.setTaxaCreditoLoja(pg[1].trim());
            mVendaPagamentoCheckout.setBandeiraCreditoLoja(pg[2].trim());
        } catch (Exception e) {
        }
    }//GEN-LAST:event_combobox1cartaocreditoItemStateChanged

